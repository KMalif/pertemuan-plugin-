import java.util.*

fun main (){

    var Scanner = Scanner(System.`in`)

    println("masukan nama : ")
    var name : String = Scanner.nextLine()

    println("masukan umur : ")
    var umur : Int = Scanner.nextInt()

    println("Tinggi : ")
    var tinggi : Boolean = Scanner.nextBoolean()

    println("masukan ipk : ")
    var ipk : Double = Scanner.nextDouble()

    println("masukan 1ps : ")
    var ips : Float = Scanner.nextFloat()

    println("nama saya $name, umur $umur tahun, ips saya $ips, ipk saya $ipk, tinggi $tinggi ")

}